SELECT EMPNO, SAL
FROM EMP a
WHERE SAL >= (SELECT MAX(SAL)
             FROM EMP
             GROUP BY DEPTNO);

SELECT EMPNO, SAL
FROM EMP
WHERE SAL >= ANY(SELECT MAX(SAL)
             FROM EMP
             GROUP BY DEPTNO);

SELECT EMPNO, SAL
FROM EMP
WHERE SAL >= ALL(SELECT MAX(SAL)
             FROM EMP
             GROUP BY DEPTNO);